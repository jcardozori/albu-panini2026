// Genera el estado inicial de todas las fichas para una sección
export function getInitialStickerState(section) {
  const stickers = {};
  for (let i = 0; i < section.totalStickers; i++) {
    stickers[i] = false; // false = vacía (blanca), true = llena (verde)
  }
  return stickers;
}

// Retorna si una sección está completamente llena
export function isSectionComplete(stickerStates) {
  if (!stickerStates) return false;
  return Object.values(stickerStates).every(v => v === true);
}

// Retorna la etiqueta de una ficha dentro de una sección
export function getStickerLabel(section, index) {
  if (section.prefix) {
    return `${section.prefix}${section.startNumber + index}`;
  }
  if (section.startNumber !== undefined) {
    return `${section.startNumber + index}`;
  }
  return `${index + 1}`;
}
